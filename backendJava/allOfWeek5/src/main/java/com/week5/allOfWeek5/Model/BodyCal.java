package com.week5.allOfWeek5.Model;

public class BodyCal {
    private int a;
    private int b;

    public BodyCal(){};

    public BodyCal(int a, int b){
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
